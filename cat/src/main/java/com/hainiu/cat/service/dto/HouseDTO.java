package com.hainiu.cat.service.dto;

import java.io.Serializable;
import java.util.Date;
import java.util.List;


public class HouseDTO implements Serializable {

    private Integer id;

    private String title;

    private String address;

    private Integer price;

    private Double area;

    private String detailUrl;

    private String norms;

    private String firstImage;

    private Integer houseType;

    private Date createTime;

    private Integer status;

    private String province;

    private String district;

    private Double minBuildArea;

    private Double maxBuildArea;

    private List<String> tags;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public Integer getPrice() {
        return price;
    }

    public void setPrice(Integer price) {
        this.price = price;
    }

    public Double getArea() {
        return area;
    }

    public void setArea(Double area) {
        this.area = area;
    }

    public String getDetailUrl() {
        return detailUrl;
    }

    public void setDetailUrl(String detailUrl) {
        this.detailUrl = detailUrl;
    }

    public String getNorms() {
        return norms;
    }

    public void setNorms(String norms) {
        this.norms = norms;
    }

    public String getFirstImage() {
        return firstImage;
    }

    public void setFirstImage(String firstImage) {
        this.firstImage = firstImage;
    }

    public Integer getHouseType() {
        return houseType;
    }

    public void setHouseType(Integer houseType) {
        this.houseType = houseType;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    public String getDistrict() {
        return district;
    }

    public void setDistrict(String district) {
        this.district = district;
    }

    public Double getMinBuildArea() {
        return minBuildArea;
    }

    public void setMinBuildArea(Double minBuildArea) {
        this.minBuildArea = minBuildArea;
    }

    public Double getMaxBuildArea() {
        return maxBuildArea;
    }

    public void setMaxBuildArea(Double maxBuildArea) {
        this.maxBuildArea = maxBuildArea;
    }

    public List<String> getTags() {
        return tags;
    }

    public void setTags(List<String> tags) {
        this.tags = tags;
    }
}
